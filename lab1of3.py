# Use this playground to experiment with list methods, using Test Run

list1 = [3, 4, 7, 9, 2]

list1.append(5) #adds 5 to the list
print (list1)

print (list1.count(4)) #counts the number of times 4 appears

list2 = [7, 8]
list1.extend(list2) #extends the list with 7 and 8
print (list1)

list1.insert(1, 0) #inserts 0 in the index 1 
print (list1)

list1.sort() #arranges the list in numerical order
print (list1)




